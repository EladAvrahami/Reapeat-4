public class FunctionJava10 {

    public static void main(String[] args) {
        //פונקציה - אוסף הוראות לביצוע רעיון משותף
    //                      ארגומנטיים
        int res = (int) Math.pow(2,3);//-pow= להעלות את המס הראשון בריבוע
        System.out.println(res);

        // תחביר פונקציה
        //public static <return value typ> functionName (<parameter list>)
        // למשל פונקציה מקבלת מספר מחיזרה את שורשו
        //public static double sqrt(int num ) - מחזירה דאבל מקבל ארגומנט 1 מסוג אינט

    }
}
