public class SpecialTabs4 {
    /*
    * \n - ירידת שורה
    *  \"-לשים מלל בהדפסה בגרשיים
    *  \t - הפרדה בין מילים של 4-8 רווחים
    * \\ - ידפיס \ לאחר מילה
    * */

    public static void main(String[] args) {
        System.out.println("hello work! \n Im Elad");
        System.out.println("\" hello world \"");
        System.out.println(" hello\tworld ");
        System.out.println("\\ hello world \\");


    }
}
